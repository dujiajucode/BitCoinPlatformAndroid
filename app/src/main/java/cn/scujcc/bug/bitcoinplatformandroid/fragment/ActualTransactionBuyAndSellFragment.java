/**
 * 现货交易买卖
 */
public class ActualTransactionBuyAndSellFragment extends BaseFragment {

    private static final String TAG = "ATBuyAndsellFragment";
    public static final String ARGS_IS_Sell = "ActualTransactionBuyAndsellFragment_IS_Sell";
    private static final String BALANCE_INFO_URL = "http://115.28.242.27:8080/balance";
    private static final String TRADE_URL = "http://115.28.242.27:8080/at/trade";

    SocketProtocol mProtocol;

    private Button mButton;

    private Spinner mSpinner;

    private ProgressDialog mDialog;

    private TextView mCNYTextView, mBTCTextView,
            mFreezedCNYTextView, mFreezedBTCTextView;

    private TextInputEditText mCountEdit, mUnivalentEdit;

    private boolean isSell;

    private boolean isLimit;


    @Override
    public void onCreate(Bundle savedInstanceState) {
        super.onCreate(savedInstanceState);

        savedInstanceState = getArguments();
        if (savedInstanceState != null) {
            isSell = savedInstanceState.getBoolean(ARGS_IS_Sell);
        }


    }

    @Override
    public void onResume() {
        super.onResume();


    }

    @Nullable
    @Override
    public View onCreateView(LayoutInflater inflater, ViewGroup container, Bundle savedInstanceState) {
        View view = inflater.inflate(R.layout.fragment_actualtransactionbuyandsell, container, false);

        mButton = (Button) view.findViewById(R.id.fragment_ats_buy_button);
        mSpinner = (Spinner) view.findViewById(R.id.fragment_actualtransactionbuy_mode);
        mCountEdit = (TextInputEditText) view.findViewById(R.id.fragment_actualtransactionbuy_count);
        mUnivalentEdit = (TextInputEditText)
                view.findViewById(R.id.fragment_actualtransactionbuy_univalent);
        mCNYTextView = (TextView) view.findViewById(R.id.fragment_ats_buy_account_cnycount);
        mBTCTextView = (TextView) view.findViewById(R.id.fragment_ats_buy_account_btccount);
        mFreezedCNYTextView = (TextView) view.findViewById(R.id.fragment_ats_buy_account_freezed_cnycount);
        mFreezedBTCTextView = (TextView) view.findViewById(R.id.fragment_ats_buy_account_freezed_btccount);

        //初始化
        if (isSell) {
            //卖出
            mButton.setText(R.string.fragment_actualtransactionbuy_sell);

        } else {
            //买入
            mButton.setText(R.string.fragment_actualtransactionbuy_buy);

        }
        mUnivalentEdit.addTextChangedListener(new TextWatcher() {


            @Override
            public void beforeTextChanged(CharSequence s, int start, int count, int after) {

            }

            @Override
            public void onTextChanged(CharSequence s, int start, int before, int count) {

            }

            @Override
            public void afterTextChanged(Editable s) {
                Log.e(TAG, "afterTextChanged" + s);
                if (s.toString().trim().length() == 0 || s.toString().equals("不用填写")) return;
                if (!isDecimal(s.toString())) {
                    mUnivalentEdit.setError("只能输入两位小数");
                } else {
                    double d = Double.parseDouble(s.toString());
                    if (d < 0.01) {
                        mUnivalentEdit.setError("最小交易0.01");
                    }
                }
            }
        });
        mCountEdit.addTextChangedListener(new TextWatcher() {


            @Override
            public void beforeTextChanged(CharSequence s, int start, int count, int after) {

            }

            @Override
            public void onTextChanged(CharSequence s, int start, int before, int count) {

            }

            @Override
            public void afterTextChanged(Editable s) {
                if (s.toString().trim().length() == 0 || s.toString().equals("不用填写")) return;
                if (!isDecimal(s.toString())) {
                    mCountEdit.setError("只能输入两位小数");
                } else {
                    double d = Double.parseDouble(s.toString());
                    if (d < 0.01) {
                        mCountEdit.setError("最小交易0.01");
                    }
                }
            }
        });

        isLimit = true;
        mSpinner.setOnItemSelectedListener(new AdapterView.OnItemSelectedListener() {
            @Override
            public void onItemSelected(AdapterView<?> parent, View view, int position, long id) {

                if (position == 0) {
                    //限价
                    isLimit = true;
                    if (isSell) {
                        mUnivalentEdit.setText("");
                        mUnivalentEdit.setEnabled(true);
                    } else {
                        mCountEdit.setText("");
                        mCountEdit.setEnabled(true);
                    }
                } else if (position == 1) {
                    //市价模式
                    isLimit = false;
                    if (isSell) {
                        mUnivalentEdit.setText("不用填写");
                        mUnivalentEdit.setEnabled(false);
                    } else {
                        mCountEdit.setText("不用填写");
                        mCountEdit.setEnabled(false);
                    }

                }
            }

            @Override
            public void onNothingSelected(AdapterView<?> parent) {

            }


        });

        mButton.setOnClickListener(new View.OnClickListener() {
            @Override
            public void onClick(View v) {
                //判断数据是否合法
                double count = 0;
                double univalent = 0;

                /*
                 * 3种情况,
                 * 1限价模式 两者都得填写
                 * 2市价买,只用填写金额
                 * 3市价卖,只用填写数量
                 */
                Log.e(TAG, "isLimit" + isLimit + "isSell" + isSell);
                try {
                    if (isLimit) {
                        count = Double.parseDouble(mCountEdit.getText().toString());
                        univalent = Double.parseDouble(mUnivalentEdit.getText().toString());
                        if (count < 0.01 || univalent < 0.01) {
                            Toast.makeText(getActivity(), "最小交易0.01", Toast.LENGTH_SHORT).show();
                            return;
                        }
                    } else if (!isSell) {
                        univalent = Double.parseDouble(mUnivalentEdit.getText().toString());
                        if (univalent < 0.01) {
                            Toast.makeText(getActivity(), "最小交易0.01", Toast.LENGTH_SHORT).show();
                            return;
                        }
                    } else if (isSell) {
                        count = Double.parseDouble(mCountEdit.getText().toString());
                        if (count < 0.01) {
                            Toast.makeText(getActivity(), "最小交易0.01", Toast.LENGTH_SHORT).show();
                            return;
                        }
                    }
                } catch (Exception e) {
                    Toast.makeText(getActivity(), "请按照规定填写", Toast.LENGTH_SHORT).show();
                    return;
                }
                //开启等待对话框
                mDialog = new ProgressDialog(getActivity());
                mDialog.setMessage("正在处理,请稍后");
                mDialog.setCanceledOnTouchOutside(false);
                mDialog.setCancelable(false);
                mDialog.show();
                //构建URL参数
                String type = "";
                if (isSell && isLimit) {
                    //卖         限价
                    type = "sell";
                } else if (isSell && !isLimit) {
                    //卖         市价
                    type = "sell_market";
                } else if (!isSell && isLimit) {
                    //买         限价
                    type = "buy";
                } else if (!isSell && !isLimit) {
                    //买         市价
                    type = "buy_market";
                }
                String price = univalent + "";
                String amount = count + "";
                RequestParameter parameter1 = new RequestParameter("api_key", SecurityConfig.USD_ACCESS_KEY);
                RequestParameter parameter2 = new RequestParameter("secret_key", SecurityConfig.USD_SECRET_KEY);
                RequestParameter parameter3 = new RequestParameter("type", type);
                RequestParameter parameter4 = new RequestParameter("price", price);
                RequestParameter parameter5 = new RequestParameter("amount", amount);
                List<RequestParameter> list = new ArrayList<>();
                list.add(parameter1);
                list.add(parameter2);
                list.add(parameter3);
                list.add(parameter4);
                list.add(parameter5);
                //执行任务
                BuyOrSellAsyncTask task = new BuyOrSellAsyncTask();
                task.execute(list);

            }
        });

        //获取余额
        updateBalance();

        //开启线程
        return view;


    }

    public boolean isDecimal(String str) {
        return Pattern.compile("^[0-9]+(.[0-9]{1,2})?$").matcher(str).matches();
    }

    public void updateBalance() {
        BalanceAsyncTask balanceAsyncTask = new BalanceAsyncTask();
        balanceAsyncTask.execute();
        Log.e(TAG, "updateBalance");
    }

    public void updateBalanceUI(final Balance balance) {
        if (getActivity() != null) {
            getActivity().runOnUiThread(new Runnable() {
                @Override
                public void run() {
                    if (balance != null) {
                        mBTCTextView.setText(Balance.getDoubleString(balance.getFreeBTC()));
                        mCNYTextView.setText(Balance.getDoubleString(balance.getFreeUSD()));
                        mFreezedCNYTextView.setText(Balance.getDoubleString(balance.getFreezedUSD()));
                        mFreezedBTCTextView.setText(Balance.getDoubleString(balance.getFreezedBTC()));
                    }
                }
            });
        }
    }

    class BalanceAsyncTask extends AsyncTask<Void, Void, Balance> {

        @Override
        protected Balance doInBackground(Void... params) {
            Balance balance = new Balance();

            try {

                RequestParameter parameter1 =
                        new RequestParameter("api_key", SecurityConfig.USD_ACCESS_KEY);

                RequestParameter parameter2 =
                        new RequestParameter("secret_key", SecurityConfig.USD_SECRET_KEY);

                String json = NetWork.requestGetUrl(BALANCE_INFO_URL, parameter1, parameter2);

                JSONObject obj = new JSONObject(json);
                obj = obj.getJSONObject("info").getJSONObject("funds");
                JSONObject freeObj = obj.getJSONObject("free");
                JSONObject freezedObj = obj.getJSONObject("freezed");
                balance.setFreeBTC(freeObj.getDouble("btc"));
                balance.setFreeUSD(freeObj.getDouble("usd"));
                balance.setFreezedBTC(freezedObj.getDouble("btc"));
                balance.setFreezedUSD(freezedObj.getDouble("usd"));

                return balance;
            } catch (Exception e) {
                e.printStackTrace();
                Log.e(TAG, "Exception " + e.getLocalizedMessage());
                sendServerError();
                return null;
            }


        }

        @Override
        protected void onPostExecute(Balance balance) {
            super.onPostExecute(balance);
            updateBalanceUI(balance);
        }
    }


    class BuyOrSellAsyncTask extends AsyncTask<List<RequestParameter>, Void, String> {

        @Override
        protected final String doInBackground(List<RequestParameter>... args) {
            try {

                List<RequestParameter> list = args[0];
                if (null == list) return null;
                return NetWork.requestPostUrl(TRADE_URL, list);
            } catch (Exception e) {
                e.printStackTrace();
                Log.e(TAG, "Exception " + e.getLocalizedMessage());
                sendServerError();
                return null;
            }


        }

        @Override
        protected void onPostExecute(String json) {
            super.onPostExecute(json);
            if (mDialog != null)
                mDialog.cancel();
        }
    }


}
